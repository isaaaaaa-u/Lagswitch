local Input = game:GetService'UserInputService'
local Enabled = 'J' -- capitalise the letter
local Disabled = 'H'-- capitalise the letter

Input.InputBegan:Connect(function(input)
    if input.KeyCode == Enum.KeyCode[Enabled] then
        settings():GetService'NetworkSettings'.IncomingReplicationLag = 99999 -- Enables lagswitch
    end
    if input.KeyCode == Enum.KeyCode[Disabled] then
        settings():GetService'NetworkSettings'.IncomingReplicationLag = 0 --Disables lagswitch
    end    
end)
